import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const tsupPath = join(root, "tsup.config.ts");
const outPath = join(root, "cli", "registry.json");

const text = readFileSync(tsupPath, "utf8");

function extractQuotedNames(block) {
  const names = [];
  const re = /"([^"]+)"/g;
  let m;
  while ((m = re.exec(block)) !== null) {
    names.push(m[1]);
  }
  return names;
}

const uiMatch = text.match(
  /const uiComponentNames = \[([\s\S]*?)\] as const/,
);
if (!uiMatch) {
  throw new Error("Could not parse uiComponentNames from tsup.config.ts");
}

const ui = extractQuotedNames(uiMatch[1]).sort();

const hooksMatch = text.match(
  /const hooksEntryNames = \[([\s\S]*?)\] as const/,
);
if (!hooksMatch) {
  throw new Error("Could not parse hooksEntryNames from tsup.config.ts");
}
const hooks = extractQuotedNames(hooksMatch[1]).sort();

const registry = {
  $schema:
    "https://json-schema.org/draft/2020-12/schema",
  description:
    "Addable UI components (src/ui) and hooks (src/hooks). Generated by scripts/generate-registry.mjs.",
  components: [...new Set([...ui, "spinner"])].sort(),
  hooks,
  nameAliases: {
    button: "buttons",
    input: "inputs",
  },
};

writeFileSync(outPath, `${JSON.stringify(registry, null, 2)}\n`, "utf8");
console.log(
  `Wrote ${outPath} (${registry.components.length} components, ${registry.hooks.length} hooks)`,
);
